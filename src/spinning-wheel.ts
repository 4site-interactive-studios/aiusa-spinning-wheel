  private addEvents() {
    const spinButton = this.wrapper.querySelector(
      ".aiusa-spinning-wheel__spin-button"
    );
    const donateButton = this.wrapper.querySelector(
      ".aiusa-spinning-wheel__donate-button"
    );
    if (spinButton) {
      spinButton.addEventListener("click", () => {
        this.spin();
      });
    }
    if (donateButton) {
      donateButton.addEventListener("click", () => {
        this.goToDonationPage();
      });
    }
    const closeButton = this.wrapper.querySelector(".close");
    if (closeButton) {
      closeButton.addEventListener("click", (e: Event) => {
        this.close(e);
      });
    }
  }
  private goToDonationPage() {
    const href = new URL(this.href);
    if (this.target && this.target !== "_self") {
      window.open(href.toString(), this.target);
      return;
    }
    window.location.href = href.toString();
  }
  private spin() {
    this.pieces = [
      ...this.wrapper.querySelectorAll(
        "#p1, #p2, #p3, #p4, #p5, #p6, #p7, #p8"
      ),
    ];

    const wheel = this.wrapper.querySelector("#the-wheel") as HTMLElement;
    const screenWidth = window.innerWidth;
    const angleOffset = screenWidth <= 850 ? screenWidth : 1000;
    // const current = this.wrapper?.querySelector(".current");
    const indicatorTimeline = gsap.timeline();
    const wheelTimeline = gsap.timeline();
    let rotation_current = +gsap.getProperty(wheel, "rotation");
    let rotation_last = rotation_current;
    // const rotation_next = rotation_current + gsap.utils.random(1800, 3600);
    const rotation_next = rotation_current + 2610;
    let tolerance = 0;
    const selectedIndex =
      this.pieces.length -
      1 -
      Math.floor(
        ((rotation_next + angleOffset) % 360) / (360 / this.pieces.length)
      );
    const indicator = this.wrapper.querySelector(
      "#wheel-indicator path"
    ) as HTMLElement;
    const buttonContainer = this.wrapper.querySelector(
      ".aiusa-spinning-wheel__buttons"
    ) as HTMLElement;

    if (wheel && indicator && buttonContainer) {
      this.log("AIUSA Spinning Wheel: spinning");
      buttonContainer.classList.add("spinning");
      buttonContainer
        .querySelector(".aiusa-spinning-wheel__spin-button")
        ?.setAttribute("disabled", "disabled");
      //indicator
      indicatorTimeline
        .to(indicator, {
          duration: 0.13,
          rotation: -15,
          transformOrigin: "70% 50%",
        })
        .to(indicator, {
          duration: 0.13,
          rotation: 3,
          ease: "power4",
        })
        .add("end");
      wheelTimeline.to(wheel, {
        duration: 5,
        rotation: rotation_next,
        transformOrigin: "50% 50%",
        ease: "power4",
        onComplete: () => {
          buttonContainer.classList.remove("spinning");
          buttonContainer.classList.add("donate");
          console.log(this.pieces[selectedIndex]);
          setTimeout(() => {
            const donateButton = this.wrapper.querySelector(
              ".aiusa-spinning-wheel__donate-button"
            );
            if (donateButton) {
              donateButton.classList.add("active");
            }
          }, 300);
        },
        onUpdate: () => {
          //recalculate tolerance
          rotation_current =
            Math.round(+gsap.getProperty(wheel, "rotation")) + 160;
          tolerance = rotation_current - rotation_last;

          //update stats
          // this.log(`rotation_last: ${rotation_last}`);
          // this.log(`rotation_current: ${rotation_current}`);
          // this.log(`tolerance: ${tolerance}`);
          // this.log(indicatorTimeline.progress());
          // this.log(wheelTimeline.progress());

          //indicator effect
          if (
            Math.round(rotation_current) % (360 / this.pieces.length) <=
            tolerance
          ) {
            if (
              indicatorTimeline.progress() > 0.2 ||
              indicatorTimeline.progress() === 0
            ) {
              indicatorTimeline.play(0);
            }
          }
          rotation_last = rotation_current;
        },
      });
    }
  }

  private loadOptions() {
    const options = this.dataset;
    if (options) {
      for (const key in options) {
        if (key in this.options) {
          this.options[key] = options[key];
          this.log(`AIUSA Spinning Wheel: ${key} = ${options[key]}`);
        }
      }
    }
  }
  private close(e: Event) {
    e.preventDefault();
    if (this.options.cookie === "true") this.createCookie();
    this.wrapper.classList.add("is-hidden");
    document.body.classList.remove("has-aiusa-spinning-wheel");
  }
  private open() {
    this.wrapper.classList.remove("is-hidden");
    document.body.classList.add("has-aiusa-spinning-wheel");
    if (this.options.cookie === "true") this.createCookie();
  }
  private createCookie() {
    const date = new Date();
    // One day Cookie
    date.setTime(date.getTime() + 24 * 60 * 60 * 1000);
    const expires = "expires=" + date.toUTCString();
    document.cookie = "aiusa_spinning_wheel=true;" + expires + ";path=/";
  }
  private readCookie() {
    const name = "aiusa_spinning_wheel=";
    const ca = document.cookie.split(";");
    for (let i = 0; i < ca.length; i++) {
      let c = ca[i];
      while (c.charAt(0) == " ") {
        c = c.substring(1);
      }
      if (c.indexOf(name) == 0) {
        return c.substring(name.length, c.length);
      }
    }
    return "";
  }
